void create_background(void) {
    zx_border(INK_CYAN);
    zx_cls(BRIGHT | INK_BLACK | PAPER_WHITE);
}

void create_game_objects(void) {
	add_player_object(100, 100, 2, 2, player_data);
	
    for (int slot = 0; slot < 10; ++slot) {
        add_game_object(slot, rand()%240, rand()%176, 1+rand()%2, rand()%2, enemy_data);
    }
}

void game_begin(void) {
    create_background();
    create_game_objects();
}

void game_collision(sprite_info_t sprite1, sprite_info_t sprite2) {
    show_collision = true;

	set_game_object_color(sprite1.slot, 30);
	set_game_object_color(sprite2.slot, 30);

    zx_border(INK_RED);  
    frames = 0;
}

void game_update(uint8_t key) {
    if (show_collision && frames >= 5) {
        show_collision = false;
        zx_border(INK_GREEN);   
		reset_game_objects();

        frames = 0;   
    }
 
    frames++;

    switch (key) {
        case 8:
            move_player(-1, 0);
            break;
        case 9:
            move_player(1, 0);
            break;
        case 10:
            move_player(0, 1);
            break;
        case 11:
            move_player(0, -1);
            break;
    }
}

void game_end(void) {}
